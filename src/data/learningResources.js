// ─────────────────────────────────────────────────────────────────────────────
// Smart selector: picks resources based on detected weaknesses
// ─────────────────────────────────────────────────────────────────────────────
export function getResourcesForWeaknesses(weaknesses = [], assessmentType = 'general') {
  const cats = new Set()

  // Primary category from test type
  const typeMap = {
    speaking:   ['speaking', 'fluency', 'pronunciation'],
    writing:    ['writing', 'grammar', 'vocabulary'],
    grammar:    ['grammar'],
    vocabulary: ['vocabulary'],
    listening:  ['listening'],
    reading:    ['reading'],
    hr:         ['interview', 'speaking', 'fluency'],
  }
  ;(typeMap[assessmentType] || ['general']).forEach(c => cats.add(c))

  // Add categories based on weaknesses text
  const w = (weaknesses || []).join(' ').toLowerCase()
  if (/grammar|tense|verb|article|preposition|agreement/.test(w)) cats.add('grammar')
  if (/vocabulary|word|lexical|range/.test(w))                    cats.add('vocabulary')
  if (/fluency|hesitat|filler|flow|natural/.test(w))              cats.add('fluency')
  if (/pronunci|accent|sound|intonation|stress|th/.test(w))       cats.add('pronunciation')
  if (/interview|professional|hr|job/.test(w))                    cats.add('interview')
  if (/writing|structure|paragraph|essay/.test(w))                cats.add('writing')

  const results = []
  const seen    = new Set()

  for (const cat of cats) {
    for (const r of (LEARNING_RESOURCES[cat] || [])) {
      if (!seen.has(r.id) && results.length < 3) {
        seen.add(r.id)
        results.push(r)
      }
    }
  }

  // Fill to at least 2 with general
  for (const r of (LEARNING_RESOURCES.general || [])) {
    if (!seen.has(r.id) && results.length < 2) {
      seen.add(r.id)
      results.push(r)
    }
  }

  // Guarantee every link/thumbnail actually works: build a YouTube search URL
  // from the channel + title (always resolves to real results) instead of
  // trusting a hand-typed video ID that may be wrong or removed, and drop the
  // thumbnail so the card falls back to its icon header rather than a broken image.
  return results.map(r => {
    const query = encodeURIComponent(`${r.channel} ${r.title}`)
    return {
      ...r,
      url: `https://www.youtube.com/results?search_query=${query}`,
      thumbnail: null,
    }
  })
}
